// Get Length
// You don't have any idea what is the type of the input object, but you know that it has Length property. (The input can be any type of object that has 
// Length property)
// 
// So, the GetLength method should return the Length of input.
// -----------------------------------------------------------

public class Kata
{
	public static int GetLength(dynamic inputObject)
	{
		return inputObject.Length;
	}
}

