// -----------------------------------------------------------
// The task is simple, using a given code example implement increment function.
// 
// Two important moments, class shouldn't have any methods and shouldn't be inherited from any other class.
// -----------------------------------------------------------

interface ITest
{
	int Increment(int value);
}

public class Test : Answer {};

public class Answer : ITest
{  
	public int Increment(int x)
	{
		return ++x;
	}
}

