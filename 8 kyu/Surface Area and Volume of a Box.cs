// -----------------------------------------------------------
// Write a function that returns the total surface area and volume of a box as an array: [area, volume]
// -----------------------------------------------------------

public class Kata
{
	public static int[] Get_size(int w, int h, int d)
	{
		int area = (w * h + w * d + h * d) * 2;
		int volume = w * h * d;
		return new int[]{ area, volume };
	}
}

